#!/usr/bin/env python3

class SolucionProblemaSubredes(object):
    def set_hosts(self, num_hosts):
        """
        Establece cuantos hosts pueden existir en la red
        """
        self.num_hosts=num_hosts
    
    def set_numsubredes(self, num_subredes):
        """
        Establece cuantos subredes pueden existir
        """
        self.num_subredes=num_subredes